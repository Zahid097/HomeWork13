package com.company;

public class Balyk {
    private String name;
    private String from;
    private byte weight;
    private byte size;

    public String getName() {
        return name;
    }
    public void setName(String name) {
        this.name = name;
    }
    public String getFrom() {
        return from;
    }
    public void setFrom(String from) {
        this.from = from;
    }
    public byte getWeight() {
        return weight;
    }
    public void setWeight(byte weight) {
        this.weight = weight;
    }
    public byte getSize() {
        return size;
    }

    public void setSize(byte size) {
        this.size = size;
    }
}
